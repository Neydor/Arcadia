    private void btnBuscarComodidadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarComodidadActionPerformed
        String a = inConsulIdComodidad.getText().toUpperCase();
        if (a.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese un dato para consultar.");
            return;
        }
        ComodidadasociadaLogica t = new ComodidadasociadaLogica();
        if (selectorBusquedaComodidad.getSelectedItem().toString() == "Nombre") {
            try {
                DefaultTableModel modelo = (DefaultTableModel) tablaComodidad.getModel();
                modelo.setNumRows(0);
                if (!t.consultarTodos().isEmpty()) {
                    for (int i = 0; i < t.consultarTodos().size(); i++) {
                        if (t.consultarTodos().get(i).getNombre().equals(a)) {
                            Object[] fila = new Object[3];
                            fila[0] = t.consultarTodos().get(i).getIdcomodidad();
                            fila[1] = t.consultarTodos().get(i).getNombre();
                            fila[2] = t.consultarTodos().get(i).getPorcentajeincremento();
                            modelo.addRow(fila);
                        }
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "No existe comodidad asociada que tenga el nombre que ha ingresado. Intentelo de nuevo.");
                    inConsulIdComodidad.setText("");
                }

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "No existe comodidad asociada que tenga el nombre que ha ingresado. Intentelo de nuevo.");
                inConsulIdComodidad.setText("");
            }
        } else if (selectorBusquedaComodidad.getSelectedItem().toString() == "ID Comodidad") {
            try {
                Comodidadasociada r = new Comodidadasociada();
                r.setIdcomodidad(Integer.parseInt(a));
                Object[] fila = new Object[3];
                fila[0] = t.consultarComodidad(r).getIdcomodidad();
                fila[1] = t.consultarComodidad(r).getNombre();
                fila[2] = t.consultarComodidad(r).getPorcentajeincremento();
                DefaultTableModel modelo = (DefaultTableModel) tablaComodidad.getModel();
                modelo.setNumRows(0);
                modelo.addRow(fila);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "No existe comodidad asociada que tenga el ID que ha ingresado. Intentelo de nuevo.");
                inConsulIdComodidad.setText("");
            }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnBuscarComodidadActionPerformed

