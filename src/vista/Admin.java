    private void btnRegisComodidadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisComodidadActionPerformed
        // TODO add your handling code here:
        ComodidadasociadaLogica c = new ComodidadasociadaLogica();
        BigDecimal b;
        if (inRegisPorcentajeComodidad.getText().equals("")) {
            b = BigDecimal.ZERO;
        } else {
            b = BigDecimal.valueOf(Double.parseDouble(inRegisPorcentajeComodidad.getText()) / 100);
        }
        if (!b.equals(BigDecimal.ZERO)
                && !inRegisPorcentajeComodidad.getText().equals("0")
                && !inRegisNombreComodidad.getText().equals("")) {
            try {
                Comodidadasociada s = new Comodidadasociada();
                s.setNombre(inRegisNombreComodidad.getText().toUpperCase());
                s.setPorcentajeincremento(b);
                c.registrarComodidad(s);
                JOptionPane.showMessageDialog(this, "Comodidad registrada");
            } catch (Exception ex) {
                System.err.println(ex.toString());
                String a = ex.getMessage();
                if (a.equals("empty String")) {
                    JOptionPane.showMessageDialog(this, "Ningun campo puede estar vacio. Intentelo de nuevo.");
                } else {
                    JOptionPane.showMessageDialog(this, "Comodidad registrada ya existe.");
                }
            }
        } else {
            if (inRegisPorcentajeComodidad.getText().equals("0")) {
                JOptionPane.showMessageDialog(this, "El porcentaje de incremento debe ser igual o mayor que 1.");
            } else {
                JOptionPane.showMessageDialog(this, "Ningun campo debe estar vacio. Intentelo de nuevo.");
            }
        }
        inRegisNombreComodidad.setText("");
        inRegisPorcentajeComodidad.setText("");
        try {
            inRegisIdComodidad.setText(String.valueOf(c.consecutivoIDComodidad()));
        } catch (Exception ex) {
            Logger.getLogger(Admin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnRegisComodidadActionPerformed

